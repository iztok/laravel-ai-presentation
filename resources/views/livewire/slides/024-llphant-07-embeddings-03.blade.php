### RAG

- LLMs have limited training data
- Context window size is limited
- Need relevant context only

#### RAG allows us to:

- Split large documents into chunks
- Convert text to vectors (embeddings)
- Find relevant chunks via vector similarity
- Feed only relevant context to LLM

