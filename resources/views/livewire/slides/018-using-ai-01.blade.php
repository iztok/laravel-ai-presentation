### Using AI

- You don't need to be a scientist to enhance your apps with AI today.
- Consider AI as a database, it runs as a separate software, we just need a way to communicate with it.
- AI interfaces are as simple at it gets. Send a human readable text and receive a human readable text.
- You can run it locally, but today's best models are available as an API with a cost per usage.
