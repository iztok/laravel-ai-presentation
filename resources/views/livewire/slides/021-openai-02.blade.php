### OpenAI-compatible APIs

- Many providers of AI models in the cloud have adopted the OpenAI API.
- In theory, you can change the API URL, and chat endpoints would accept the same.
    - The OpenAI PHP package validates the responses, which caused issues for me with non-OpenAI APIs.
- I recommend you check [openrouter.ai](https://openrouter.ai/) too.
    - With one API, you get access to 273 models (Mistral, Google, Llama).
